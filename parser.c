#include "philo.h"

long	ft_atol(char *str)
{
	long	result;
	int		sign;

	result = 0;
	sign = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-' && sign == 1)
			sign = -1;
		else if (*str == '-' && sign == -1)
			sign = 1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	return (result * sign);
}

void	check_and_parse(t_table *table, char **av)
{
	table->philo_nbr = ft_atol(av[1]);
}
